var add = function(number1, number2) {
  return number1 + number2;
};

var number1 = parseInt(prompt("Let's add. Enter a number:"));
var number2 = parseInt(prompt("Enter another number to add to the previous one:"));
var result = add(number1, number2);
alert(result);

